public class Books {
    String bookName;
    String authorName;
    String publishingHouse;
    int totalPages;

    public Books(){

    }
    public Books(String bookName,String authorName,String publishingHouse,int totalPages){
        this.bookName=bookName;
        this.authorName=authorName;
        this.publishingHouse=publishingHouse;
        this.totalPages=totalPages;
    }


}



